  pg_get_serial_sequence('ESTUDIANTE',        'id'),
  COALESCE((SELECT MAX(id)        FROM ESTUDIANTE),        1),
  (SELECT COUNT(*) FROM ESTUDIANTE) > 0
);
SELECT setval(
  pg_get_serial_sequence('SALA',              'id'),
  COALESCE((SELECT MAX(id)        FROM SALA),              1),
  (SELECT COUNT(*) FROM SALA) > 0
);
